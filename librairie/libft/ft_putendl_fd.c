#include "libft.h"

void	ft_putendl_fd(char *s, int fd)
{
	int	i;

	if (!s)
		return ;
	i = 0;
	while (s[i])
	{
		if (fd > -1)
			write(fd, &s[i], 1);
		i++;
	}
	write(fd, "\n", 1);
}
