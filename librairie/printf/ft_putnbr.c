#include "ft_printf.h"

void	ft_putnbr(long n, size_t *len)
{
	if (n < 0)
	{
		ft_putchar('-', len);
		n *= -1;
	}
	if (n > 9)
		ft_putnbr(n / 10, len);
	ft_putchar((n % 10 + '0'), len);
}
